

//Operadores de asignacion, incremento y decremento


let a = 1;

document.write('Valor de a: ', a );
document.write('<br>');

a++; //Aumenta valor en 1
document.write('Valor de a: ', a );
document.write('<br>');

a += 1;//Aumento variable
document.write('Valor de a: ', a );
document.write('<br>');

a-- ; //Decremento de 1
document.write('Valor de a: ', a );
document.write('<br>');

a -= 3; //Decremento de 3
document.write('Valor de a: ', a );
document.write('<br>');

a *= 3; //Multiplicar por 2
document.write('Valor de a: ', a );
document.write('<br>');

a /= 4; //Dividir por 4
document.write('Valor de a: ', a );
document.write('<br>');

a **= 5; //Potenciador por 5
document.write('Valor de a: ', a );
document.write('<br>');